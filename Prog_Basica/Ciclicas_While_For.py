#Bucle while
print("\nINICIO BUCLE WHILE\n")
contador = int(input("Introduce un número: "))
while contador <= 10:
    print(f"Número {contador}")
    contador += 1
print("\nFin del bucle while\n")

# Bucle for
print("\nINICIO BUCLE FOR\n")
for i in range(1, 6):
    print(f"Número {i}")
print("\nFin del bucle FOR\n")